using System;

namespace Trianguleiro
{
    public class Triangulo
    {
        public double posicaoX, posicaoY, posicaoZ;

        public Triangulo(double posicaoX, double posicaoY, double posicaoZ) {
            this.posicaoX = posicaoX;
            this.posicaoY = posicaoY;
            this.posicaoZ = posicaoZ;
        }

        public Triangulo() { }
    }
}
